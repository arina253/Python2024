# or
# True or True ======> True
# True or False =====> True
# False or True =====> True
# False or False ====> False

print (5 > 2 or 4 < 7)
print (3 == 3 or 2 > 4)
print (3 != 3 or 5 > 3)
print (2 == 7 or 7 > 8)

# not 

# not True ====> False
# not False ====> True

